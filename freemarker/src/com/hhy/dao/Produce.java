package com.hhy.dao;

public class Produce {
	private String produceImageUrl;
	private String produceTitle;
	private String produceDescription;
	
	public String getProduceImageUrl() {
		return produceImageUrl;
	}
	public void setProduceImageUrl(String produceImageUrl) {
		this.produceImageUrl = produceImageUrl;
	}
	public String getProduceTitle() {
		return produceTitle;
	}
	public void setProduceTitle(String produceTitle) {
		this.produceTitle = produceTitle;
	}
	public String getProduceDescription() {
		return produceDescription;
	}
	public void setProduceDescription(String produceDescription) {
		this.produceDescription = produceDescription;
	}
}
